export function getIconComponent(iconName: string): LucideIcon {
  const icon = ICON_MAP[iconName];
  if (!icon) {
    console.warn(`[icon-mapper] Icon "${iconName}" not found, using Package as fallback`);
    return Package;
  }
  return icon;
}

